if __name__ == "__main__":

    retriever = FaissReviewRetriever()

    test_query = "The food arrived cold and the delivery was very late."

    print("=" * 80)
    print("Query:")
    print(test_query)
    print("=" * 80)

    results = retriever.retrieve_with_sentiment_filter(
        query=test_query,
        sentiment="negative",
        top_k=3,
        candidate_k=10,
    )

    for result in results:
        print()
        print(f"Result {result['rank']}")
        print("-" * 80)
        print(f"Score: {result['score']:.4f}")
        print(f"Sentiment: {result['sentiment']}")
        print(f"Original Yelp label: {result['label']}")
        print(f"Review text: {result['text'][:700]}")